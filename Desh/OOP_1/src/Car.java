public class Car {
    // instance variables
    int no;         // car number
    String name;    // car name
    String color;   // car color
    double weight;  // car weight
    int speed;      // car speed

    // instance methods
    public void speed() {   // speed method
        System.out.println(speed + " km/h");
    }    
    
    public void color() {   // color method
        System.out.println(color);
    }
}


